int		ft_strequ(char const *s1, char const *s2)
{
	if (s1 && s2)
	{
		while (*s1)
			if (*s1++ != *s2++)
				return (0);
		return (*s1 == *s2);
	}
	else if (!s1 != !s2)
		return (0);
	else
		return (1);
}
