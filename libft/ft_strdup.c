#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;

	if (!(tmp = malloc(ft_strlen(s1) + 1)))
		return ((char*)NULL);
	ft_strcpy(tmp, s1);
	return (tmp);
}
