#include "libft.h"

void	*ft_memcpy(void *restrict dst, const void *restrict src, size_t n)
{
	char		*d;
	const char	*s;

	d = dst;
	s = src;
	if (d == s)
		return (dst);
	while (n-- > 0)
		*d++ = *s++;
	return (dst);
}
