int		ft_sqrt(int nb)
{
	int i;
	int j;
	int sum;

	i = 1;
	j = 0;
	sum = 0;
	while (sum <= nb)
	{
		if (sum == nb)
			return (j);
		sum += i;
		i += 2;
		j++;
	}
	return (0);
}
