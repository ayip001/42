int		ft_str_is_alpha(char *str)
{
	char *tmp;

	tmp = str;
	while (*tmp)
	{
		if (!(*tmp >= 'a' && *tmp <= 'z') && !(*tmp >= 'A' && *tmp <= 'Z'))
			return (0);
		tmp++;
	}
	return (1);
}
