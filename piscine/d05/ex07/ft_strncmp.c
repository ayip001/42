int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int				i;
	unsigned char	c1;
	unsigned char	c2;

	if (n == 0)
		return (0);
	i = 0;
	while (s1[i] == s2[i] && --n)
		if (s1[i++] == '\0')
			return (0);
	c1 = s1[i];
	c2 = s2[i];
	return (c1 - c2);
}
