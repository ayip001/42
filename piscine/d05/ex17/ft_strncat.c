char	*ft_strncat(char *dest, char *src, int nb)
{
	char *tmp;

	tmp = dest;
	while (*tmp)
		tmp++;
	while ((*tmp++ = *src++))
		if (nb-- <= 0)
		{
			tmp--;
			*tmp = 0;
			return (dest);
		}
	return (dest);
}
