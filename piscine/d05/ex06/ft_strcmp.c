int		ft_strcmp(char *s1, char *s2)
{
	int				i;
	unsigned char	c1;
	unsigned char	c2;

	i = 0;
	while (s1[i] == s2[i])
		if (s1[i++] == '\0')
			return (0);
	c1 = s1[i];
	c2 = s2[i];
	return (c1 - c2);
}
